#25.要素の一括真偽判定
#all
tesst_list_1=[0,100,300,000,150,0,40]
tesst_list_2=[[],[],[]]
tesst_list_3=[['python','izum','com'],['www'],['https']]

print(all(tesst_list_1))
print(all(tesst_list_2))
print(all(tesst_list_3))

